function unique(values = []) {
  return [...new Set(values.filter(Boolean))];
}

function toMathsTutorEntity(record) {
  const boardTags = unique([record.board, record.subBoard, record.examType]).slice(0, 3);
  const topicTags = unique(record.topics ?? []);
  const localityTags = unique(record.sectors ?? []);
  const serviceModeTags = unique(record.serviceModes ?? record.mode ?? []);
  const schoolFitTags = unique(record.schoolFocus ?? []);

  return {
    ...record,
    tutorName: record.name,
    boardSpecialization: record.subBoard ?? record.examType ?? record.board,
    classesSupported: record.classFocus ?? record.classLevel,
    examSupport: record.examType ? [record.examType] : [],
    yearsExperience: record.experience,
    startingFee: record.price,
    shortBio: record.description ?? record.summary,
    boardTags,
    topicTags,
    localityTags,
    serviceModeTags,
    schoolFitTags,
  };
}

const mathsTutors = rawMathsTutors.map(toMathsTutorEntity);

function dedupeById(items) {
  const seen = new Set();

  return items.filter((item) => {
    if (!item || seen.has(item.id)) {
      return false;
    }

    seen.add(item.id);
    return true;
  });
}

export function getMathsTutorById(id) {
  return mathsTutors.find((tutor) => tutor.id === id) ?? null;
}

export function getMathsTutorBySlug(slug) {
  const normalizedSlug = String(slug ?? "").trim().toLowerCase();

  return mathsTutors.find((tutor) => tutor.slug === normalizedSlug) ?? null;
}

export function getMathsTutorsByIds(ids = []) {
  return ids.map((id) => getMathsTutorById(id)).filter(Boolean);
}

export function getMathsTutorsForPage(pageKey, featuredTutorIds = []) {
  const prioritisedTutors = getMathsTutorsByIds(featuredTutorIds);
  const relatedTutors = mathsTutors.filter((tutor) => tutor.featuredOn.includes(pageKey));

  return dedupeById([...prioritisedTutors, ...relatedTutors]).slice(0, 15);
}

export { mathsTutors };
